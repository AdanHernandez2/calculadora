class Calculadora {
    // metodos de la clase Calculadora sumar, restar, dividir, multiplicar
    sumar (num1, num2) {
        return num1 + num2;
    }
    restar (num1, num2) {
        return num1 - num2;
    }
    dividir (num1, num2) {
        return num1 / num2;
    }
    multiplicar (num1, num2) {
        return num1 * num2;
    }
}